void main() {
  int num1 = 8;
  int num2 = 8;
  var res;

  res = num1 > num2;
  print("num1 lebih besar dari num2 : $res");

  res = num1 < num2;
  print("num1 kurang dari num2 : $res");

  res = num1 >= num2;
  print("num1 lebih dari sama dengan num2 : $res");

  res = num1 <= num2;
  print("num1 kurang dari sama dengan num2 : $res");

  res = num1 == num2;
  print("num1 sama dengan num2 : $res");

  res = num1 != num2;
  print("num1 tidak sama dengan num2 : $res");
}
